int main()
{
    string s;
    cin >> s;

    int frq[26] = {0};
    for (int i = 0; i < s.size(); i++)
    {
        for (char j = 'a'; j <= 'z'; j++)
        {
            if (s[i] == j)
            {
                frq[s[i] - 'a']++;
            }
        }
    }

    for (int i = 0; i < 26; i++)
    {
        char a = i + 'a';
        if (frq[i])
        {
            cout << a << " : " << frq[i] << endl;
        }
    }

    return 0;
}